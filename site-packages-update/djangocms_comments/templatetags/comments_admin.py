from django.template.loader_tags import register


@register.inclusion_tag('djangocms_comments/admin_submit_line.html', takes_context=True)
def submit_row(context):
    """
    Displays the row of buttons for delete and save.
    """
    from django.contrib.admin.templatetags.admin_modify import submit_row
    row = submit_row(context)
    row['is_soft_deleted'] = context['original'].moderated == 'deleted'
    return row

@register.inclusion_tag('djangocms_comments/fields_cleanblog.html')
def fields_cleanblog_render(field, **kwargs):
    show_label = kwargs.get('show_label', None)
    horizontal_label_class = kwargs.get('horizontal_label_class', None)
    valid_message = kwargs.get('valid_message', None)
    return {
        'field': field,
        'show_label': show_label,
        'horizontal_label_class': horizontal_label_class,
        'valid_message':valid_message
    }
